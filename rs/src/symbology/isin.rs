/// ISIN is an ISO 6166 International Securities Identifying Number.
///
/// ISINs consist of three parts: a two letter country code, a nine character alpha-numeric national
/// security identifier, and a single check digit. The country code is the ISO 3166-1 alpha-2 code
/// for the country of issue. The nine-digit security identifier is the National Securities
/// Identifying Number (NSIN). The check digit is computed using the Luhn algorithm.

const ISIN_LENGTH: usize = 12;
const ISIN_CHECK_SUM_INDEX: usize = ISIN_LENGTH - 1;
const ISIN_COUNTRY_LENGTH: usize = 2;
const TEN: i32 = 10;

/// Validates the country code and the check digit of the ISIN.
pub fn validate(isin: &str) -> Result<(), String> {
    if !validate_country(isin) {
        return Err("unknown country code: invalid ISIN".to_string());
    }

    validate_check_digit(isin)
}

/// Validates the check digit of the ISIN.
pub fn validate_check_digit(isin: &str) -> Result<(), String> {
    let bytes = isin.as_bytes();

    if bytes.len() != ISIN_LENGTH {
        return Err("length should be 12 symbols: invalid ISIN".to_string());
    }

    let n = bytes[ISIN_CHECK_SUM_INDEX];
    if n < b'0' || n > b'9' {
        return Err("last symbol should be a digit 0-9: invalid ISIN".to_string());
    }

    let n = n - b'0';

    let d = calculate_check_digit(isin)?;

    if n != d {
        return Err("invalid check digit (last symbol): invalid ISIN".to_string());
    }

    Ok(())
}

/// Calculates a check digit of the ISIN according to the Luhn algorithm.
pub fn calculate_check_digit(isin: &str) -> Result<u8, String> {
    let bytes = isin.as_bytes();

    if bytes.len() < ISIN_CHECK_SUM_INDEX {
        return Err("length should be at least 11 symbols: invalid ISIN".to_string());
    }

    let mut sum: i32 = 0;
    let mut multiply = true;

    let mut i = ISIN_CHECK_SUM_INDEX as i32 - 1;
    while i >= 0 {
        let idx = i as usize;
        let mut n = to_ordinal_number_isin(bytes[idx], idx)?;

        if n < TEN {
            if multiply {
                n *= 2;
                sum += n % TEN + n / TEN;
            } else {
                sum += n;
            }

            multiply = !multiply;
        } else {
            if multiply {
                sum += n / TEN;
                n %= TEN;
            } else {
                sum += n % TEN;
                n /= TEN;
            }

            n *= 2;
            sum += n % TEN + n / TEN;
        }

        i -= 1;
    }

    sum = (TEN - sum % TEN) % TEN;

    Ok(sum as u8)
}

/// Validates if the first two letters of the ISIN represent a valid country code.
pub fn validate_country(isin: &str) -> bool {
    let bytes = isin.as_bytes();

    if bytes.len() < ISIN_COUNTRY_LENGTH {
        return false;
    }

    let first = bytes[0];
    let second = bytes[1];

    match first {
        b'A' => matches!(second,
            b'D' | b'E' | b'F' | b'G' | b'I' | b'L' | b'M' | b'N' |
            b'O' | b'Q' | b'R' | b'S' | b'T' | b'U' | b'W' | b'Z'
        ),
        b'B' => matches!(second,
            b'A' | b'B' | b'D' | b'E' | b'F' | b'G' | b'H' | b'I' |
            b'J' | b'M' | b'N' | b'O' | b'R' | b'S' | b'T' | b'V' |
            b'W' | b'Y' | b'Z'
        ),
        b'C' => matches!(second,
            b'A' | b'C' | b'D' | b'F' | b'G' | b'H' | b'I' | b'K' |
            b'L' | b'M' | b'N' | b'O' | b'R' | b'U' | b'V' | b'X' |
            b'Y' | b'Z'
        ),
        b'D' => matches!(second, b'E' | b'J' | b'K' | b'M' | b'O' | b'Z'),
        b'E' => matches!(second, b'C' | b'E' | b'G' | b'R' | b'S' | b'T' | b'U'),
        b'F' => matches!(second, b'I' | b'J' | b'K' | b'M' | b'O' | b'R'),
        b'G' => matches!(second,
            b'A' | b'B' | b'D' | b'E' | b'G' | b'H' | b'I' | b'L' |
            b'M' | b'N' | b'Q' | b'R' | b'S' | b'T' | b'U' | b'W' |
            b'Y'
        ),
        b'H' => matches!(second, b'K' | b'M' | b'N' | b'R' | b'T' | b'U'),
        b'I' => matches!(second,
            b'D' | b'E' | b'L' | b'M' | b'N' | b'O' | b'Q' | b'R' |
            b'S' | b'T'
        ),
        b'J' => matches!(second, b'E' | b'M' | b'O' | b'P'),
        b'K' => matches!(second,
            b'E' | b'G' | b'H' | b'I' | b'M' | b'N' | b'P' | b'R' |
            b'W' | b'Y' | b'Z'
        ),
        b'L' => matches!(second,
            b'A' | b'B' | b'C' | b'I' | b'K' | b'R' | b'S' | b'T' |
            b'U' | b'V' | b'Y'
        ),
        b'M' => matches!(second,
            b'A' | b'C' | b'D' | b'E' | b'G' | b'H' | b'K' | b'L' |
            b'M' | b'N' | b'O' | b'P' | b'R' | b'S' | b'T' | b'U' |
            b'V' | b'W' | b'X' | b'Y' | b'Z'
        ),
        b'N' => matches!(second,
            b'A' | b'C' | b'E' | b'F' | b'G' | b'I' | b'L' | b'O' |
            b'P' | b'R' | b'S' | b'U' | b'Z'
        ),
        b'O' => second == b'M',
        b'P' => matches!(second,
            b'A' | b'E' | b'F' | b'G' | b'H' | b'K' | b'L' | b'M' |
            b'N' | b'S' | b'T' | b'W' | b'Y'
        ),
        b'Q' => matches!(second,
            b'A' | b'Q' | b'R' | b'S' | b'T' | b'U' | b'V' | b'W' |
            b'X' | b'Y' | b'Z'
        ),
        b'R' => matches!(second, b'O' | b'U' | b'W'),
        b'S' => matches!(second,
            b'A' | b'B' | b'C' | b'D' | b'E' | b'G' | b'H' | b'I' |
            b'K' | b'L' | b'M' | b'N' | b'O' | b'R' | b'S' | b'T' |
            b'V' | b'Y' | b'Z'
        ),
        b'T' => matches!(second,
            b'C' | b'D' | b'F' | b'G' | b'H' | b'J' | b'K' | b'L' |
            b'M' | b'N' | b'O' | b'R' | b'T' | b'V' | b'W' | b'Z'
        ),
        b'U' => matches!(second, b'A' | b'G' | b'M' | b'S' | b'Y' | b'Z'),
        b'V' => matches!(second, b'A' | b'C' | b'E' | b'G' | b'I' | b'N' | b'U'),
        b'W' => matches!(second, b'F' | b'S'),
        b'X' => matches!(second, b'A' | b'B' | b'C' | b'D' | b'F' | b'K' | b'L' | b'S'),
        b'Y' => matches!(second, b'E' | b'T'),
        b'Z' => matches!(second, b'A' | b'M' | b'W'),
        _ => false,
    }
}

fn to_ordinal_number_isin(b: u8, i: usize) -> Result<i32, String> {
    if i < ISIN_COUNTRY_LENGTH {
        return match b {
            b'A'..=b'Z' => Ok((b - b'A') as i32 + TEN),
            _ => Err(format!(
                "symbol at position {} should be a letter A-Z: invalid ISIN",
                i
            )),
        };
    }

    match b {
        b'0'..=b'9' => Ok((b - b'0') as i32),
        b'A'..=b'Z' => Ok((b - b'A') as i32 + TEN),
        _ => Err(format!(
            "symbol at position {} should be either a digit 0-9 or a letter A-Z: invalid ISIN",
            i
        )),
    }
}

