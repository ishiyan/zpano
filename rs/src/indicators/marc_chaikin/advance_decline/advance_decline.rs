/// Parameters for the Advance-Decline indicator.
/// Advance-Decline requires HLCV bar data and has no configurable parameters.
pub struct AdvanceDeclineParams;

impl Default for AdvanceDeclineParams {
    fn default() -> Self {
        Self
    }
}

