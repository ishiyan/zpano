/// icalc — command-line indicator calculator.
///
/// Reads a JSON settings file containing indicator definitions,
/// creates indicator instances, prints their metadata, then iterates
/// through embedded bar data printing bar values and all indicator
/// outputs on each iteration.
///
/// Usage: icalc <settings.json>

use portf::entities::bar::Bar;
use portf::entities::scalar::Scalar;
use portf::indicators::core::identifier::Identifier;
use portf::indicators::core::indicator::{Indicator, Output};
use portf::indicators::core::outputs::band::Band;
use portf::indicators::core::outputs::metadata::OutputMetadata;
use portf::indicators::factory::{create_indicator, JsonValue};

use std::env;
use std::fs;
use std::process;

fn main() {
    let args: Vec<String> = env::args().collect();
    if args.len() < 2 {
        eprintln!("usage: icalc <settings.json>");
        process::exit(1);
    }

    let data = fs::read_to_string(&args[1]).unwrap_or_else(|e| {
        eprintln!("error reading settings file: {}", e);
        process::exit(1);
    });

    let parsed = JsonValue::parse(&data).unwrap_or_else(|e| {
        eprintln!("error parsing settings file: {}", e);
        process::exit(1);
    });

    let entries = parsed.as_array().unwrap_or_else(|| {
        eprintln!("error: settings file must be a JSON array");
        process::exit(1);
    });

    let mut indicators: Vec<Box<dyn Indicator>> = Vec::with_capacity(entries.len());

    for entry in entries {
        let id_str = entry.get("identifier")
            .and_then(|v| v.as_str_val())
            .unwrap_or_else(|| {
                eprintln!("error: entry missing 'identifier' string");
                process::exit(1);
            });

        let ident = Identifier::from_str(id_str).unwrap_or_else(|| {
            eprintln!("error: unknown indicator identifier: {}", id_str);
            process::exit(1);
        });

        let params_json = match entry.get("params") {
            Some(p) => json_value_to_string(p),
            None => "{}".to_string(),
        };

        let ind = create_indicator(ident, &params_json).unwrap_or_else(|e| {
            eprintln!("error creating indicator {}: {}", id_str, e);
            process::exit(1);
        });

        indicators.push(ind);
    }

    print_metadata(&indicators);

    let bars = test_bars();

    println!();
    println!("=== Bar Data & Indicator Outputs ===");
    println!();

    for (i, bar) in bars.iter().enumerate() {
        let date = epoch_to_date_string(bar.time);
        println!(
            "Bar[{:3}] {}  O={:.4} H={:.4} L={:.4} C={:.4} V={:.0}",
            i, date, bar.open, bar.high, bar.low, bar.close, bar.volume
        );

        for ind in indicators.iter_mut() {
            let meta = ind.metadata();
            let output = ind.update_bar(bar);

            print!("  {:<45} primed={:<5} ", meta.mnemonic, ind.is_primed());
            print_output(&meta.outputs, &output);
            println!();
        }

        println!();
    }
}

// ---------------------------------------------------------------------------
// Metadata printing
// ---------------------------------------------------------------------------

fn print_metadata(indicators: &[Box<dyn Indicator>]) {
    println!("=== Indicator Metadata ===");
    println!();

    for (i, ind) in indicators.iter().enumerate() {
        let meta = ind.metadata();
        println!("[{}] {}", i, meta.mnemonic);
        println!("  Identifier:  {}", meta.identifier);
        println!("  Description: {}", meta.description);
        println!("  Outputs ({}):", meta.outputs.len());

        for (j, out) in meta.outputs.iter().enumerate() {
            println!(
                "    [{}] kind={} shape={} mnemonic={:?} description={:?}",
                j, out.kind, out.shape, out.mnemonic, out.description
            );
        }

        // Build metadata JSON manually
        let mut json = String::from("{\n");
        json.push_str(&format!("    \"identifier\": {},\n", meta.identifier as u8));
        json.push_str(&format!("    \"mnemonic\": {:?},\n", meta.mnemonic));
        json.push_str(&format!("    \"description\": {:?},\n", meta.description));
        json.push_str("    \"outputs\": [\n");
        for (k, out) in meta.outputs.iter().enumerate() {
            json.push_str("      {\n");
            json.push_str(&format!("        \"kind\": {},\n", out.kind));
            json.push_str(&format!("        \"shape\": {},\n", out.shape as u8));
            json.push_str(&format!("        \"mnemonic\": {:?},\n", out.mnemonic));
            json.push_str(&format!("        \"description\": {:?}\n", out.description));
            if k < meta.outputs.len() - 1 {
                json.push_str("      },\n");
            } else {
                json.push_str("      }\n");
            }
        }
        json.push_str("    ]\n");
        json.push_str("  }");

        // Indent each line by 2 spaces
        let indented: String = json.lines()
            .map(|line| format!  ("  {}", line))
            .collect::<Vec<_>>()
            .join("\n");
        println!("  Full metadata JSON:\n{}", indented);
        println!();
    }
}

// ---------------------------------------------------------------------------
// Output printing
// ---------------------------------------------------------------------------

fn print_output(outputs_meta: &[OutputMetadata], output: &Output) {
    for (i, val) in output.iter().enumerate() {
        let fallback = format!("out[{}]", i);
        let name = if i < outputs_meta.len() {
            outputs_meta[i].mnemonic.as_str()
        } else {
            &fallback
        };

        if let Some(s) = val.downcast_ref::<Scalar>() {
            if s.value.is_nan() {
                print!("{}=NaN ", name);
            } else {
                print!("{}={:.4} ", name, s.value);
            }
        } else if let Some(b) = val.downcast_ref::<Band>() {
            if b.is_empty() {
                print!("{}=Band(NaN) ", name);
            } else {
                print!("{}=Band({:.4},{:.4}) ", name, b.lower, b.upper);
            }
        } else {
            print!("{}=? ", name);
        }
    }
}

// ---------------------------------------------------------------------------
// Date formatting
// ---------------------------------------------------------------------------

fn epoch_to_date_string(epoch: i64) -> String {
    let days = epoch / 86400;
    // Convert days since Unix epoch to y-m-d using a civil calendar algorithm.
    let z = days + 719468;
    let era = if z >= 0 { z } else { z - 146096 } / 146097;
    let doe = (z - era * 146097) as u32;
    let yoe = (doe - doe / 1460 + doe / 36524 - doe / 146096) / 365;
    let y = (yoe as i64) + era * 400;
    let doy = doe - (365 * yoe + yoe / 4 - yoe / 100);
    let mp = (5 * doy + 2) / 153;
    let d = doy - (153 * mp + 2) / 5 + 1;
    let m = if mp < 10 { mp + 3 } else { mp - 9 };
    let y = if m <= 2 { y + 1 } else { y };
    format!("{:04}-{:02}-{:02}", y, m, d)
}

// ---------------------------------------------------------------------------
// JSON value to string (for passing params to factory)
// ---------------------------------------------------------------------------

fn json_value_to_string(val: &JsonValue) -> String {
    match val {
        JsonValue::Null => "null".to_string(),
        JsonValue::Bool(b) => b.to_string(),
        JsonValue::Number(n) => {
            if *n == (*n as i64) as f64 && n.abs() < 1e15 {
                format!("{}", *n as i64)
            } else {
                format!("{}", n)
            }
        }
        JsonValue::Str(s) => format!("{:?}", s),
        JsonValue::Array(arr) => {
            let items: Vec<String> = arr.iter().map(json_value_to_string).collect();
            format!("[{}]", items.join(","))
        }
        JsonValue::Object(pairs) => {
            let items: Vec<String> = pairs.iter()
                .map(|(k, v)| format!("{:?}:{}", k, json_value_to_string(v)))
                .collect();
            format!("{{{}}}", items.join(","))
        }
    }
}

