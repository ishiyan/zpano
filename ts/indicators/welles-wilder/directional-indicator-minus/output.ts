/** Enumerates output values of the Directional Indicator Minus indicator. */
export enum DirectionalIndicatorMinusOutput {
  /** The Directional Indicator Minus (-DI) value. */
  DirectionalIndicatorMinusValue = 0,
  /** The Directional Movement Minus (-DM) value. */
  DirectionalMovementMinusValue = 1,
  /** The Average True Range (ATR) value. */
  AverageTrueRangeValue = 2,
  /** The True Range (TR) value. */
  TrueRangeValue = 3,
}
