/** Enumerates output values of the Directional Movement Index indicator. */
export enum DirectionalMovementIndexOutput {
  /** The Directional Movement Index (DX) value. */
  DirectionalMovementIndexValue = 0,
  /** The Directional Indicator Plus (+DI) value. */
  DirectionalIndicatorPlusValue = 1,
  /** The Directional Indicator Minus (-DI) value. */
  DirectionalIndicatorMinusValue = 2,
  /** The Directional Movement Plus (+DM) value. */
  DirectionalMovementPlusValue = 3,
  /** The Directional Movement Minus (-DM) value. */
  DirectionalMovementMinusValue = 4,
  /** The Average True Range (ATR) value. */
  AverageTrueRangeValue = 5,
  /** The True Range (TR) value. */
  TrueRangeValue = 6,
}
