export enum Periodicity {
    /**
     * The daily periodicity.
     */
    DAILY = 0,

    /**
     * The weekly periodicity.
     */
    WEEKLY = 1,

    /**
     * The monthly periodicity.
     */
    MONTHLY = 2,

    /**
     * The quarterly periodicity.
     */
    QUARTERLY = 3,

    /**
     * The annual periodicity.
     */
    ANNUAL = 4
}
