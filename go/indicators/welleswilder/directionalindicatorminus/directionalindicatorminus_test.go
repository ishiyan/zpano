func TestDirectionalIndicatorMinusConstructor(t *testing.T) {
	t.Parallel()

	dim, err := NewDirectionalIndicatorMinus(14)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	if dim.Length() != 14 {
		t.Errorf("expected length 14, got %d", dim.Length())
	}

	if dim.IsPrimed() {
		t.Error("should not be primed initially")
	}

	_, err = NewDirectionalIndicatorMinus(0)
	if err == nil {
		t.Error("expected error for length 0")
	}

	_, err = NewDirectionalIndicatorMinus(-8)
	if err == nil {
		t.Error("expected error for negative length")
	}
}

func TestDirectionalIndicatorMinusIsPrimed(t *testing.T) {
	t.Parallel()

	high := testInputHigh()
	low := testInputLow()
	close_ := testInputClose()

	t.Run("length=14", func(t *testing.T) {
		t.Parallel()

		dim, _ := NewDirectionalIndicatorMinus(14)

		for i := 0; i < 14; i++ {
			dim.Update(close_[i], high[i], low[i])

			if dim.IsPrimed() {
				t.Errorf("[%d] should not be primed yet", i)
			}
		}

		dim.Update(close_[14], high[14], low[14])
		if !dim.IsPrimed() {
			t.Error("[14] should be primed")
		}
	})
}

func TestDirectionalIndicatorMinusUpdate(t *testing.T) {
	t.Parallel()

	const tolerance = 1e-8

	high := testInputHigh()
	low := testInputLow()
	close_ := testInputClose()
	expected := testExpectedDI14()
	dim, _ := NewDirectionalIndicatorMinus(14)

	for i := range high {
		act := dim.Update(close_[i], high[i], low[i])

		if math.IsNaN(expected[i]) {
			if !math.IsNaN(act) {
				t.Errorf("[%d] expected NaN, got %v", i, act)
			}

			continue
		}

		if math.IsNaN(act) {
			t.Errorf("[%d] expected %v, got NaN", i, expected[i])
			continue
		}

		if math.Abs(act-expected[i]) > tolerance {
			t.Errorf("[%d] expected %v, got %v", i, expected[i], act)
		}
	}
}

