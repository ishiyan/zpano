const std = @import("std");

/// CUSIP (Committee on Uniform Security Identification Procedures) validation.
///
/// The CUSIP number consists of a base number of six characters known as the
/// issuer number and a two character suffix known as the issue number. The 9th
/// character is a check digit.
///
/// See https://www.cusip.com/static/html/cusipaccess/CUSIPIntro_%207.26.2007.pdf.
const cusip_length = 9;
const cusip_check_sum_index = cusip_length - 1;
const cusip_issue_second_index = cusip_length - 2;
const cusip_issue_first_index = cusip_length - 3;
const ten = 10;

pub const CusipError = error{
    InvalidCusip,
};

/// Validate the CUSIP.
pub fn validate(cusip: []const u8) CusipError!void {
    if (cusip.len < cusip_length) {
        return error.InvalidCusip;
    }

    const n = cusip[cusip_check_sum_index];
    if (n < '0' or n > '9') {
        return error.InvalidCusip;
    }

    const n_val = n - '0';
    const d = try calculateCheckDigit(cusip);

    if (n_val != d) {
        // A fix for incorrect CUSIPs in SEC 13F Security List.
        if (cusip[cusip_issue_first_index] == '9') {
            switch (cusip[cusip_issue_second_index]) {
                '0', '5' => return,
                else => {},
            }
        }

        return error.InvalidCusip;
    }
}

/// Calculate a check digit of the CUSIP according to the Luhn algorithm.
pub fn calculateCheckDigit(cusip: []const u8) CusipError!u8 {
    if (cusip.len < cusip_check_sum_index) {
        return error.InvalidCusip;
    }

    var total: u32 = 0;

    for (0..cusip_check_sum_index) |i| {
        var n = toOrdinalNumber(cusip[i]) orelse return error.InvalidCusip;

        if (i % 2 == 1) {
            n *= 2;
        }

        total += n / ten + n % ten;
    }

    total = (ten - total % ten) % ten;

    return @intCast(total);
}

fn toOrdinalNumber(b: u8) ?u32 {
    if (b >= '0' and b <= '9') {
        return @as(u32, b - '0');
    }
    if (b >= 'A' and b <= 'Z') {
        return @as(u32, b - 'A') + ten;
    }
    return switch (b) {
        '*' => 36,
        '@' => 37,
        '#' => 38,
        else => null,
    };
}

